//셸 정렬(버전 2 : h = ```, 13,4,1)
#include <stdio.h>
#include <stdlib.h>

//셸 정렬 함수
void shell(int a[], int n)
{
    int h;
    for(h = 1; h < n/9; h = h*3+1)
    ;   //h의 초깃값을 구함
        //1부터 시작하여 값을 3배하고 1을 더하면서 n/9를 넘지 않는 가장 큰 값을 h에 대입함
    for(; h > 0; h/= 3)
    {
        for(int i = h; i < n; i++)
        {
            int tmp = a[i];
            for(int j = i - h; j >= 0 && a[j] > tmp; j -= h)
                a[j+h] = a[j];
            a[j+h] = tmp;
        }
    }
}

int main(void)
{
    int nx, *x;
    printf("셸 정렬\n");
    printf("요소 개수 : ");
    scanf("%d", &nx);
    x = calloc(nx, sizeof(int));
    for(int i = 0; i < nx; i++)
    {
        printf("x[%d] : ", i);
        scanf("%d", &x[i]);
    }
    shell(x, nx);   //배열 x를 셸 정렬
    
    printf("오름차순으로 정렬했습니다.\n");
    for(int i = 0; i < nx; i++)
    {
        printf("x[%d] = %d\n", i, x[i]);
    }
    free(x);

    return 0;
}